package com.dare2date.domein.lastfm;

import java.util.Calendar;
import java.util.Date;

public class LastfmEvent implements Comparable<LastfmEvent>{
    private int id;
    private String title;
    private Calendar startDate;
    private String city;
    private String country;
    private String street;
    private String venueName;

    public String getVenueName() {
        return venueName;
    }

    public void setVenueName(String venueName) {
        this.venueName = venueName;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setStartDate(Calendar startDate) {
        this.startDate = startDate;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public LastfmEvent(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public Calendar getStartDate() {
        return startDate;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }

    public String getStreet() {
        return street;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        LastfmEvent otherevent = (LastfmEvent) obj;
        if(otherevent.getId() != this.getId()) {
            return false;
        }
        return true;
    }

    public int compareTo(LastfmEvent o) {
       if(o.getId() == this.id) {
           return 0;
       } else if(o.getId() > this.id) {
           return -1;
       } else {
           return 1;
       }

    }
}
